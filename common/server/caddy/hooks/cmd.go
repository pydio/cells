package hooks

var RestartChan chan bool
var StopChan chan bool

func init() {
	RestartChan = make(chan bool, 1)
	StopChan = make(chan bool, 1)
}

func Restart() error {
	select {
	case RestartChan <- true:
	default:
		break
	}
	return nil
}

func Stop() error {
	StopChan <- true
	return nil
}
